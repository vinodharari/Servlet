package com.vinod;
class LoginForm 
{
	private String username;
	private String password;

	public void setUsername(String name){
		this.username=name;
	}
	public String getUsername(){
		return username;
	}

	public void setPassword(String word){
		this.password=word;
	}
	public String getPassword(){
		return password;
	}

}
